const filterMainPoints = (min, max) => {
    return mainPoints.filter(item => item.value >= min && item.value <= max);
};

const getMin = (points) => {
    return points.reduce((min, p) => p.value < min ? p.value : min, points[0].value);
};
const getMax = (points) => {
    return points.reduce((max, p) => p.value > max ? p.value : max, points[0].value);
};

/**
 * A {@link ImageryLayerCatalogItem} representing an imagery layer from Cesium Ion.
 *
 * @alias HeatmapImageryCatalogItem
 * @constructor
 * @extends ImageryLayerCatalogItem
 *
 * @param {Terria} terria The Terria instance.
 */
var HeatmapImageryCatalogItem = function(terria) {
    ImageryLayerCatalogItem.call(this, terria);

    /**
     * Gets or sets whether this WMS server has been identified as supporting the COLORSCALERANGE parameter.
     * @type {Boolean}
     */
    this.supportsColorScaleRange = true;

    /**
     * Gets or sets the minumum of the color scale range.  Because COLORSCALERANGE is a non-standard
     * property supported by ncWMS servers, this property is ignored unless {@link WebMapServiceCatalogItem#supportsColorScaleRange}
     * is true.  {@link WebMapServiceCatalogItem#colorScaleMaximum} must be set as well.
     * @type {Number}
     */
    this.colorScaleMinimum = 0;

    /**
     * Gets or sets the maximum of the color scale range.  Because COLORSCALERANGE is a non-standard
     * property supported by ncWMS servers, this property is ignored unless {@link WebMapServiceCatalogItem#supportsColorScaleRange}
     * is true.  {@link WebMapServiceCatalogItem#colorScaleMinimum} must be set as well.
     * @type {Number}
     */
    this.colorScaleMaximum = 2000;

    knockout.track(this, ['colorScaleMinimum', 'colorScaleMaximum']);

    knockout.getObservable(this, 'colorScaleMinimum').subscribe(function() {
        updateMinMax(this);
    }, this);

    knockout.getObservable(this, 'colorScaleMaximum').subscribe(function() {
        updateMinMax(this);
    }, this);
};

